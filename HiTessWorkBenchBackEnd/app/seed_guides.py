def seed_default_guides(db):
    """DEFAULT_GUIDES 중 동일 title이 없는 항목만 삽입합니다. 멱등성 보장."""
    existing_titles = {g.title for g in db.query(models.UserGuide.title).all()}
    inserted = 0
    for guide_data in DEFAULT_GUIDES:
        if guide_data["title"] not in existing_titles:
            db.add(models.UserGuide(**guide_data))
            inserted += 1
    if inserted:
        db.commit()
