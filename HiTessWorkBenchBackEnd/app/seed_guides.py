def seed_default_guides(db):
    """DEFAULT_GUIDES를 upsert 방식으로 삽입/갱신합니다."""
    existing_guides = {g.title: g for g in db.query(models.UserGuide).all()}
    inserted = 0
    updated = 0
    for guide_data in DEFAULT_GUIDES:
        if guide_data["title"] in existing_guides:
            guide = existing_guides[guide_data["title"]]
            guide.content = guide_data["content"]
            guide.category = guide_data["category"]
            updated += 1
        else:
            db.add(models.UserGuide(**guide_data))
            inserted += 1
    if inserted or updated:
        db.commit()
